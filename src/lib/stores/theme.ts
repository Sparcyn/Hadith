/**
 * ============================================================================
 * ملف: theme.ts
 * الوصف: مخزن إدارة السمة (الوضع الفاتح/الداكن/السيبيا)
 * ============================================================================
 * 
 * هذا الملف يدير سمة التطبيق (الألوان والمظهر)
 * يدعم ثلاث سمات: فاتح، داكن، سيبيا
 * يحفظ تفضيل المستخدم في localStorage
 * 
 * الاستخدام:
 * import { theme } from '$lib/stores/theme';
 * theme.cycle();           // التبديل بين السمات
 * theme.set('dark');       // تعيين سمة محددة
 * ============================================================================
 */

// استيراد دالة writable لإنشاء مخزن تفاعلي
import { writable } from 'svelte/store';

// استيراد متغير browser للتحقق من بيئة التشغيل
import { browser } from '$app/environment';

// ============================================================================
// تعريف الأنواع
// ============================================================================

// نوع السمة - يقبل فقط هذه القيم الثلاث
type Theme = 'light' | 'dark' | 'sepia';

/**
 * دالة إنشاء مخزن السمة
 * تُرجع كائن المخزن مع دوال التحكم
 */
function createThemeStore() {
	
	// ========================================================================
	// تحديد السمة الافتراضية
	// ========================================================================
	
	// جلب السمة المحفوظة أو استخدام تفضيل النظام
	const defaultTheme: Theme = browser 
		// في المتصفح: جلب من localStorage أو تفضيل النظام
		? (localStorage.getItem('theme') as Theme) || 
		  // التحقق من تفضيل النظام للوضع الداكن
		  (window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light')
		// في الخادم: استخدام الوضع الفاتح افتراضياً
		: 'light';
	
	// ========================================================================
	// إنشاء المخزن التفاعلي
	// ========================================================================
	const { subscribe, set, update } = writable<Theme>(defaultTheme);
	
	/**
	 * تطبيق السمة على عنصر HTML الجذر
	 * يُضيف أو يُزيل classes حسب السمة المختارة
	 * @param theme - السمة المراد تطبيقها
	 */
	function applyTheme(theme: Theme) {
		// التحقق من وجود المتصفح
		if (browser) {
			// إزالة جميع classes السمات السابقة
			document.documentElement.classList.remove('dark', 'sepia');
			
			// إضافة class السمة الجديدة (إلا إذا كانت light)
			if (theme !== 'light') {
				document.documentElement.classList.add(theme);
			}
		}
	}
	
	// ========================================================================
	// إرجاع كائن المخزن مع الدوال
	// ========================================================================
	return {
		// دالة الاشتراك للمكونات
		subscribe,
		
		/**
		 * التبديل الدوري بين السمات
		 * light -> sepia -> dark -> light ...
		 */
		cycle: () => {
			update(current => {
				// ترتيب السمات للتبديل
				const themes: Theme[] = ['light', 'sepia', 'dark'];
				
				// حساب فهرس السمة التالية
				const nextIndex = (themes.indexOf(current) + 1) % themes.length;
				
				// الحصول على السمة التالية
				const newTheme = themes[nextIndex];
				
				// حفظ السمة في localStorage
				if (browser) {
					localStorage.setItem('theme', newTheme);
				}
				
				// تطبيق السمة على الصفحة
				applyTheme(newTheme);
				
				// إرجاع السمة الجديدة
				return newTheme;
			});
		},
		
		/**
		 * تعيين سمة محددة مباشرة
		 * @param theme - السمة المراد تعيينها
		 */
		set: (theme: Theme) => {
			// حفظ في localStorage
			if (browser) {
				localStorage.setItem('theme', theme);
			}
			
			// تطبيق السمة
			applyTheme(theme);
			
			// تحديث المخزن
			set(theme);
		}
	};
}

// ============================================================================
// تصدير المخزن
// نسخة واحدة لكل التطبيق (Singleton)
// ============================================================================
export const theme = createThemeStore();
