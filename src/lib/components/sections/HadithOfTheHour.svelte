<!--
	============================================================================
	ملف: HadithOfTheHour.svelte
	الوصف: مكون حديث الساعة
	============================================================================
	
	هذا المكون يعرض "حديث الساعة" بتصميم إسلامي أنيق
	يُستخدم كعنصر رئيسي في الصفحة الرئيسية لعرض الأحاديث النبوية الشريفة
	
	المميزات:
	- عرض نص الحديث بالعربية مع دعم RTL
	- عرض اسم الراوي ومصدر الحديث
	- خاصية النسخ إلى الحافظة مع تأكيد بصري
	- خاصية المشاركة على وسائل التواصل
	- نافذة إنشاء صور قابلة للمشاركة
	- تتبع الأحاديث المشاهدة مؤخراً
	- تصميم متجاوب (موبايل أولاً)
	
	طريقة الاستخدام:
	<HadithOfTheHour hadith={بيانات_الحديث} />
	
	الخصائص (Props):
	- hadith: كائن الحديث (اختياري) يحتوي على:
	  * id: المعرف الفريد
	  * arabicText: نص الحديث بالعربية
	  * narrator: اسم الراوي
	  * collection: المصدر (مثل صحيح مسلم)
	  * reference: المرجع التفصيلي
	
	الاعتماديات:
	- lucide-svelte: أيقونات الأزرار
	- recentlyViewed: مخزن الأحاديث المشاهدة مؤخراً
	- ImageGeneratorModal: نافذة إنشاء الصور
	============================================================================
-->

<script lang="ts">
	// =========================================================================
	// الاستيرادات
	// =========================================================================
	
	// استيراد الأيقونات من مكتبة Lucide
	// Copy: أيقونة النسخ | Share2: أيقونة المشاركة | Check: علامة الصح
	// Image: أيقونة الصورة | Clock: أيقونة الساعة
	import { Copy, Share2, Check, Image, Clock } from 'lucide-svelte';
	
	// مخزن الأحاديث المشاهدة مؤخراً - يحفظ سجل المستخدم
	import { recentlyViewed } from '$lib/stores/recentlyViewed';
	
	// مكون النافذة المنبثقة لإنشاء صور الأحاديث
	import { ImageGeneratorModal } from '$lib/components/ui';
	
	// =========================================================================
	// تعريف الأنواع (TypeScript)
	// =========================================================================
	
	/**
	 * واجهة خصائص المكون
	 * تحدد هيكل بيانات الحديث المُمررة للمكون
	 */
	interface Props {
		hadith?: {
			id: string;          // المعرف الفريد للحديث
			arabicText: string;  // نص الحديث بالعربية
			narrator: string;    // اسم الراوي (مثل: أبو هريرة رضي الله عنه)
			collection: string;  // المصدر (مثل: صحيح مسلم)
			reference: string;   // المرجع التفصيلي (مثل: كتاب المساجد، حديث ٦٧١)
		};
	}
	
	// =========================================================================
	// الخصائص والحالة
	// =========================================================================
	
	/**
	 * استخراج الخصائص مع قيمة افتراضية للحديث
	 * الحديث الافتراضي يُعرض عندما لا يتم تمرير بيانات
	 * هذا يضمن أن المكون دائماً لديه محتوى للعرض
	 */
	let { hadith = {
		id: '1',
		arabicText: 'إِنَّ أَحَبَّ بِلَادِ اللَّهِ إِلَى اللَّهِ مَسَاجِدُهَا، وَأَبْغَضَ بِلَادِ اللَّهِ إِلَى اللَّهِ أَسْوَاقُهَا',
		narrator: 'أبو هريرة رضي الله عنه',
		collection: 'صحيح مسلم',
		reference: 'كتاب المساجد، حديث ٦٧١'
	} }: Props = $props();
	
	/**
	 * حالة تفاعلية لزر النسخ
	 * true = تم النسخ (يظهر علامة صح)
	 * false = الحالة الطبيعية (يظهر أيقونة النسخ)
	 * يعود للحالة الطبيعية بعد ثانيتين
	 */
	let copied = $state(false);
	
	/**
	 * التحكم في ظهور نافذة إنشاء الصور
	 * true = النافذة مفتوحة
	 * false = النافذة مغلقة
	 */
	let showImageModal = $state(false);
	
	// =========================================================================
	// معالجات الأحداث (Event Handlers)
	// =========================================================================
	
	/**
	 * دالة نسخ نص الحديث
	 * تنسخ النص العربي إلى حافظة المستخدم
	 * تستخدم Clipboard API الحديثة
	 * تُظهر تأكيد بصري (علامة صح) لمدة ثانيتين
	 */
	async function copyText(): Promise<void> {
		// نسخ النص إلى الحافظة
		await navigator.clipboard.writeText(hadith.arabicText);
		
		// تفعيل حالة "تم النسخ"
		copied = true;
		
		// إعادة الحالة للوضع الطبيعي بعد ثانيتين
		setTimeout(() => copied = false, 2000);
	}
	
	/**
	 * معالج النقر على "قراءة المزيد"
	 * يُضيف الحديث الحالي إلى سجل المشاهدة
	 * يُمكّن خاصية "شوهد مؤخراً" في التطبيق
	 */
	function handleReadMore(): void {
		recentlyViewed.add({
			id: hadith.id,
			text: hadith.arabicText,
			collection: hadith.collection,
			narrator: hadith.narrator
		});
	}
	
	/**
	 * فتح نافذة إنشاء الصور
	 * تسمح للمستخدم بإنشاء صور قابلة للمشاركة من الحديث
	 */
	function openImageGenerator(): void {
		showImageModal = true;
	}
	
	/**
	 * إغلاق نافذة إنشاء الصور
	 * تُستدعى عند إلغاء المستخدم أو إتمام إنشاء الصورة
	 */
	function closeImageGenerator(): void {
		showImageModal = false;
	}
</script>

